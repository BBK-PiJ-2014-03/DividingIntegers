public class Calculator {
    
    
    public void add (int n, int i) {
        System.out.println(n+i);
    }
    public void subtract (int n, int i) {
        System.out.println(n-i);
    }
    public void multiply (int n, int i) {
        System.out.println(n*i);
    }
    public void divide (int n, int i) {
        System.out.println((double)n/(double)i);
    }
    public void modulus (int n, int i) {
        System.out.println((double)n%(double)i);
    }
}